    public ArrayList<VeiculoI> filterVehicles(){
        Stream<VeiculoI> fv = main.vehicleArrayList.stream().filter(v -> v.getEstado().getAtual() == "Disponivel" || v.getEstado().getAtual() == "Novo" );
        ArrayList<VeiculoI> lv = new ArrayList(fv.collect(Collectors.toList()));
        return lv;
    }
    public ArrayList<Cliente> filterClients(){
        Stream<Cliente> fc = main.clientsArrayList.stream().filter(c-> !main.findClientRent(c));
        ArrayList<Cliente> lc = new ArrayList(fc.collect(Collectors.toList()));
        return lc;
    }
   
    public void updateTables(){
        this.FilteredvehicleArrayList = filterVehicles();
        this.FilteredclientsArrayList = filterClients();
        Object ColumnsCliente[] = {"Nome"};
        Object RowsCliente[][] = new Object[this.FilteredclientsArrayList.size()][1];
        for (int i = 0; i < this.FilteredclientsArrayList.size(); i++){
            RowsCliente[i][0] = this.FilteredclientsArrayList.get(i).getNome();
        }
        Object ColumnsVeiculo[] = {"Placa", "Valor DiÃ¡rio"};
        Object RowsVeiculo[][] = new Object[this.FilteredvehicleArrayList.size()][2];
        for(int i = 0; i< this.FilteredvehicleArrayList.size(); i++){
            RowsVeiculo[i][0] = this.FilteredvehicleArrayList.get(i).getPlaca();
            RowsVeiculo[i][1] = this.FilteredvehicleArrayList.get(i).getValorDiariaLocacao();
        }
        DefaultTableModel modelClientes = new DefaultTableModel(RowsCliente, ColumnsCliente);
        DefaultTableModel modelVeiculos = new DefaultTableModel(RowsVeiculo, ColumnsVeiculo);
        FilteredClienteList.setModel(modelClientes);
        FilteredVehicleList.setModel(modelVeiculos);
        jScrollPane1.setViewportView(FilteredClienteList);
        jScrollPane3.setViewportView(FilteredVehicleList);
    }

